"""
Generate user-facing wiki-style Word (.docx) documentation for 7 widgets.
Format: step-by-step instructions as seen by the user in the settings panel.
Run: python3 generate_docs_v2.py
"""
from docx import Document
from docx.shared import Pt, RGBColor, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.oxml.ns import qn
from docx.oxml import OxmlElement

BRAND_BLUE = RGBColor(0x4F, 0x6A, 0xFF)
DARK       = RGBColor(0x1E, 0x29, 0x3B)
GRAY       = RGBColor(0x64, 0x74, 0x8B)
NOTE_BG    = 'EEF1FF'
TIP_BG     = 'F0FDF4'


def set_cell_bg(cell, hex_color):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), hex_color.lstrip('#'))
    tcPr.append(shd)


def make_doc():
    doc = Document()
    for section in doc.sections:
        section.top_margin    = Cm(2.0)
        section.bottom_margin = Cm(2.0)
        section.left_margin   = Cm(2.5)
        section.right_margin  = Cm(2.5)
    style = doc.styles['Normal']
    style.font.name = 'Calibri'
    style.font.size = Pt(10.5)
    style.font.color.rgb = DARK
    return doc


def title(doc, name, subtitle):
    p = doc.add_paragraph()
    r = p.add_run(name)
    r.bold = True
    r.font.size = Pt(28)
    r.font.color.rgb = BRAND_BLUE
    p2 = doc.add_paragraph()
    r2 = p2.add_run(subtitle)
    r2.font.size = Pt(12)
    r2.font.color.rgb = GRAY
    doc.add_paragraph()


def h2(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(10)
    p.paragraph_format.space_after  = Pt(2)
    r = p.add_run(text)
    r.bold = True
    r.font.size = Pt(14)
    r.font.color.rgb = BRAND_BLUE


def h3(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(6)
    p.paragraph_format.space_after  = Pt(1)
    r = p.add_run(text)
    r.bold = True
    r.font.size = Pt(11.5)
    r.font.color.rgb = DARK


def body(doc, text):
    p = doc.add_paragraph(text)
    p.runs[0].font.size = Pt(10.5)
    p.paragraph_format.space_after = Pt(2)


def steps(doc, items):
    """Numbered list."""
    for text in items:
        p = doc.add_paragraph(style='List Number')
        r = p.add_run(text)
        r.font.size = Pt(10.5)


def bullets(doc, items):
    """Bulleted list."""
    for text in items:
        p = doc.add_paragraph(style='List Bullet')
        r = p.add_run(text)
        r.font.size = Pt(10.5)


def note(doc, text, bg=NOTE_BG):
    table = doc.add_table(rows=1, cols=1)
    cell = table.rows[0].cells[0]
    set_cell_bg(cell, bg)
    p = cell.paragraphs[0]
    p.paragraph_format.left_indent  = Cm(0.3)
    p.paragraph_format.right_indent = Cm(0.3)
    p.paragraph_format.space_before = Pt(4)
    p.paragraph_format.space_after  = Pt(4)
    r = p.add_run(text)
    r.font.size = Pt(10)
    r.font.italic = True
    r.font.color.rgb = RGBColor(0x3D, 0x55, 0xE8)
    doc.add_paragraph()


def screenshot(doc, caption):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(4)
    r = p.add_run(f'[Скриншот: {caption}]')
    r.font.size = Pt(9)
    r.font.italic = True
    r.font.color.rgb = GRAY
    doc.add_paragraph()


def sep(doc):
    doc.add_paragraph()


